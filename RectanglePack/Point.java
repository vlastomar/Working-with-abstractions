package RectanglePack;

public class Point {
    private int coordinateX, getCoordinateY;

    public Point(int coordinateX, int getCoordinateY){
        this.coordinateX = coordinateX;
        this.getCoordinateY = getCoordinateY;
    }

    public int getCoordinateX() {
        return this.coordinateX;
    }

    public void setCoordinateX(int coordinateX) {
        this.coordinateX = coordinateX;
    }

    public int getCoordinateY() {
        return this.getCoordinateY;
    }

    public void setCoordinateY(int getCoordinateY) {
        this.getCoordinateY = getCoordinateY;
    }

}
